import { SendHorizontal } from "lucide-react";
import { useState } from "react";
// import EmojiPicker from "../shared/EmojiPicker";
import { useChatStore } from "../store/useChatStore";

const MessageInput = () => {
  const [input, setInput] = useState("");
  const { sendMessage, selectedUser } = useChatStore();

  const handleSendMessage = (e) => {
    e.preventDefault();
    if (!input.trim()) return;
    sendMessage({ receiverId: selectedUser?._id, message: input });
    setInput("");
  };

  return (
    <form
      onSubmit={handleSendMessage}
      className="flex flex-col sm:flex-row items-center gap-2 w-full p-2"
    >
      <EmojiPicker onChange={(emoji) => setInput((prev) => prev + emoji)} />
      <input
        type="text"
        placeholder="Type your message..."
        value={input}
        onChange={(e) => setInput(e.target.value)}
        className="input input-bordered w-full"
      />
      <button type="submit" className="btn btn-primary min-h-[44px]">
        <SendHorizontal className="size-5" />
      </button>
    </form>
  );
};

export default MessageInput;